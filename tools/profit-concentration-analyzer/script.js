document.addEventListener("DOMContentLoaded", function () {

  const calculateButton = document.getElementById("calculateButton");
  const shareButton = document.getElementById("shareWhatsAppButton");
  const resultContainer = document.getElementById("result");

  function showError(message) {
    resultContainer.innerHTML = "<p style='color:#b91c1c;font-weight:600'>" + message + "</p>";
  }

  function runDiagnostic() {

    const revenues = [
      parseFloat(document.getElementById("rev1").value) || 0,
      parseFloat(document.getElementById("rev2").value) || 0,
      parseFloat(document.getElementById("rev3").value) || 0,
      parseFloat(document.getElementById("rev4").value) || 0,
      parseFloat(document.getElementById("rev5").value) || 0,
      parseFloat(document.getElementById("rev6").value) || 0
    ];

    const margins = [
      parseFloat(document.getElementById("margin1").value) || 0,
      parseFloat(document.getElementById("margin2").value) || 0,
      parseFloat(document.getElementById("margin3").value) || 0,
      parseFloat(document.getElementById("margin4").value) || 0,
      parseFloat(document.getElementById("margin5").value) || 0,
      parseFloat(document.getElementById("margin6").value) || 0
    ];

    const profits = [];

    for (let i = 0; i < revenues.length; i++) {
      const profit = revenues[i] * (margins[i] / 100);
      profits.push(profit);
    }

    const totalProfit = profits.reduce(function (a, b) {
      return a + b;
    }, 0);

    if (totalProfit <= 0) {
      showError("Total estimated profit must be greater than zero to evaluate profit concentration.");
      return;
    }

    const sortedProfits = profits.slice().sort(function (a, b) {
      return b - a;
    });

    const topSegment = sortedProfits[0];
    const topTwoSegments = sortedProfits[0] + sortedProfits[1];

    const topShare = (topSegment / totalProfit) * 100;
    const topTwoShare = (topTwoSegments / totalProfit) * 100;

    let concentrationMessage = "";

    if (topShare > 60) {
      concentrationMessage =
        "The analysis indicates that a very large proportion of profit is generated by a single revenue segment. This structure suggests that the economic engine of the business is heavily dependent on a narrow activity set.";
    } else if (topTwoShare > 70) {
      concentrationMessage =
        "The majority of profit is generated by two dominant segments. While the business may appear diversified from a revenue perspective, the profit engine is still concentrated in a limited number of activities.";
    } else {
      concentrationMessage =
        "Profit generation appears relatively distributed across several revenue segments. This structure typically indicates a broader profit engine with less dependence on a single activity.";
    }

    const report =
      "<h3>Diagnostic Summary</h3>" +
      "<p>Total estimated profit across all segments is calculated from the combination of revenue and gross margin inputs. The largest segment generates approximately " +
      topShare.toFixed(1) +
      "% of total profit, while the two largest segments together generate roughly " +
      topTwoShare.toFixed(1) +
      "% of total profit. " +
      concentrationMessage +
      "</p>" +

      "<h3>Operational Interpretation</h3>" +
      "<p>When profit is concentrated in a small number of activities management attention, pricing strategy, and operational resources naturally gravitate toward those areas. Businesses often discover that a few products, services, or customer groups quietly subsidize the rest of the organization. Understanding where profit is actually generated helps operators focus commercial energy on activities that materially influence overall performance.</p>" +

      "<h3>Structural Risk Observation</h3>" +
      "<p>High profit concentration can create structural vulnerability. If a single product line, service category, or customer group weakens due to competition, supplier disruption, or pricing pressure the financial impact on the business can be immediate. A diversified profit base generally creates greater resilience because multiple activities contribute meaningfully to overall profitability.</p>" +

      "<h3>Management Questions</h3>" +
      "<p>Which revenue segments generate the majority of gross profit in our business and are management resources aligned with those areas?</p>" +
      "<p>If the highest profit segment weakened, how would the overall profitability of the business change?</p>" +
      "<p>Are there revenue streams consuming operational effort that contribute relatively little profit?</p>" +

      "<p>This calculator evaluates only one narrow dimension of business structure. In practice, structural diagnostics examine the interaction between profit drivers, cost structure, capital deployment, cash flow timing, revenue concentration, supplier dynamics, and forward operating scenarios. This type of analysis requires structured financial data and careful modelling. Only a limited number of businesses are worked with at any given time because meaningful analysis requires detailed operational understanding. If the thinking behind this diagnostic resonates with how you evaluate your business structure you are welcome to explore whether there may be scope to work together by using the Contact page to request a deeper structural diagnostic review.</p>";

    resultContainer.innerHTML = report;

  }

  calculateButton.addEventListener("click", runDiagnostic);

  shareButton.addEventListener("click", function () {

    const url = window.location.href;

    const shareLink =
      "https://api.whatsapp.com/send?text=" +
      encodeURIComponent("Useful diagnostic tool: " + url);

    window.open(shareLink, "_blank");

  });

});