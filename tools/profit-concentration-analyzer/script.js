document.addEventListener("DOMContentLoaded", function () {

  const calculateButton = document.getElementById("calculateButton");
  const shareButton = document.getElementById("shareWhatsAppButton");
  const resultContainer = document.getElementById("result");

  calculateButton.addEventListener("click", function () {

    const segments = [];

    for (let i = 1; i <= 6; i++) {

      const revenueInput = document.getElementById("segment" + i + "Revenue");
      const marginInput = document.getElementById("segment" + i + "Margin");

      const revenueValue = parseFloat(revenueInput.value);
      const marginValue = parseFloat(marginInput.value);

      if (i <= 2) {

        if (isNaN(revenueValue) || isNaN(marginValue)) {

          resultContainer.innerHTML = "<p>Please complete revenue and margin for the first two segments.</p>";
          return;

        }

      }

      if (!isNaN(revenueValue) && !isNaN(marginValue)) {

        const profit = revenueValue * (marginValue / 100);

        segments.push({
          revenue: revenueValue,
          margin: marginValue,
          profit: profit
        });

      }

    }

    if (segments.length === 0) {

      resultContainer.innerHTML = "<p>No valid segment data provided.</p>";
      return;

    }

    let totalProfit = 0;

    segments.forEach(function (segment) {

      totalProfit += segment.profit;

    });

    segments.sort(function (a, b) {

      return b.profit - a.profit;

    });

    const topSegmentProfit = segments[0].profit;
    const topTwoProfit = segments.slice(0, 2).reduce(function (sum, seg) {
      return sum + seg.profit;
    }, 0);

    const topSegmentShare = (topSegmentProfit / totalProfit) * 100;
    const topTwoShare = (topTwoProfit / totalProfit) * 100;

    let interpretation = "";
    let structuralImplication = "";
    let strategicObservation = "";

    if (topTwoShare >= 70) {

      interpretation = "A large proportion of profit is generated by a small number of segments.";
      structuralImplication = "Operational earnings appear concentrated within a narrow part of the revenue structure. If these activities weaken the overall profit base could deteriorate rapidly.";
      strategicObservation = "Management attention should focus on protecting the economics of the dominant segments while evaluating whether additional profit engines can be developed.";

    } else if (topTwoShare >= 50) {

      interpretation = "Profit shows moderate concentration within several leading segments.";
      structuralImplication = "The business relies meaningfully on a few core activities although secondary segments contribute measurable earnings.";
      strategicObservation = "Operators may benefit from examining whether smaller segments can improve margin performance or scale profit contribution.";

    } else {

      interpretation = "Profit contribution appears distributed across multiple segments.";
      structuralImplication = "The business structure does not depend heavily on a single activity to sustain margin generation.";
      strategicObservation = "A diversified profit base can provide resilience against shifts in demand, supplier cost pressure, or competitive disruption.";

    }

    const report =
      "<h3>Calculated Metrics</h3>" +
      "<p>Total estimated profit: " + totalProfit.toFixed(2) + "</p>" +
      "<p>Profit share of largest segment: " + topSegmentShare.toFixed(1) + "%</p>" +
      "<p>Profit share of two largest segments: " + topTwoShare.toFixed(1) + "%</p>" +
      "<h3>Interpretation</h3>" +
      "<p>" + interpretation + "</p>" +
      "<h3>Structural Implications</h3>" +
      "<p>" + structuralImplication + "</p>" +
      "<h3>Strategic Observation</h3>" +
      "<p>" + strategicObservation + "</p>" +
      "<p>This tool evaluates only one narrow dimension of a business structure. Full diagnostic engagements examine the interaction between profit drivers, cost structure, capital deployment, cash flow timing, revenue concentration, supplier leverage, and forward scenario modelling.</p>" +
      "<p>The calculator therefore acts only as a simplified preview of the analytical framework used inside those engagements.</p>" +
      "<p>Because this work requires detailed modelling and careful interpretation MJB Strategic intentionally works with a limited number of companies at any given time.</p>" +
      "<p>If the thinking behind this tool resonates with how you evaluate your business you are welcome to reach out to determine whether there may be a sensible fit to work together.</p>";

    resultContainer.innerHTML = report;

  });

  shareButton.addEventListener("click", function () {

    const pageUrl = window.location.href;
    const shareUrl = "https://api.whatsapp.com/send?text=" + encodeURIComponent(pageUrl);

    window.open(shareUrl, "_blank");

  });

});