document.addEventListener("DOMContentLoaded", function () {

  const calculateButton = document.getElementById("calculateButton");
  const shareButton = document.getElementById("shareWhatsAppButton");
  const resultContainer = document.getElementById("result");

  function showError(message) {
    resultContainer.innerHTML = "<p style='color:#b91c1c;font-weight:600'>" + message + "</p>";
  }

  function runDiagnostic() {

    const revenues = [
      parseFloat(document.getElementById("rev1").value),
      parseFloat(document.getElementById("rev2").value),
      parseFloat(document.getElementById("rev3").value) || 0,
      parseFloat(document.getElementById("rev4").value) || 0,
      parseFloat(document.getElementById("rev5").value) || 0,
      parseFloat(document.getElementById("rev6").value) || 0
    ];

    const margins = [
      parseFloat(document.getElementById("margin1").value),
      parseFloat(document.getElementById("margin2").value),
      parseFloat(document.getElementById("margin3").value) || 0,
      parseFloat(document.getElementById("margin4").value) || 0,
      parseFloat(document.getElementById("margin5").value) || 0,
      parseFloat(document.getElementById("margin6").value) || 0
    ];

    if (isNaN(revenues[0]) || isNaN(revenues[1]) || isNaN(margins[0]) || isNaN(margins[1])) {
      showError("Please complete the required revenue and margin fields before running the diagnostic.");
      return;
    }

    let profits = [];

    for (let i = 0; i < revenues.length; i++) {
      const profit = revenues[i] * (margins[i] / 100);
      profits.push(profit);
    }

    const totalProfit = profits.reduce(function (sum, value) {
      return sum + value;
    }, 0);

    if (totalProfit === 0) {
      showError("Profit cannot be calculated because the inputs result in zero total profit.");
      return;
    }

    const sortedProfits = profits.slice().sort(function (a, b) {
      return b - a;
    });

    const topSegmentShare = (sortedProfits[0] / totalProfit) * 100;
    const topTwoShare = ((sortedProfits[0] + sortedProfits[1]) / totalProfit) * 100;

    let concentrationInterpretation = "";

    if (topSegmentShare > 60) {
      concentrationInterpretation = "Profit appears heavily concentrated in a single revenue segment.";
    } else if (topTwoShare > 70) {
      concentrationInterpretation = "Profit appears concentrated in a small number of revenue segments.";
    } else {
      concentrationInterpretation = "Profit appears relatively diversified across multiple revenue segments.";
    }

    const report =
      "<h2>Diagnostic Summary</h2>" +
      "<p>This diagnostic estimates the profit contribution of each revenue segment based on the revenue and gross margin percentages provided. The analysis indicates that approximately " +
      topSegmentShare.toFixed(1) +
      "% of total profit is generated by the single most profitable segment, while the two largest segments together generate roughly " +
      topTwoShare.toFixed(1) +
      "% of total profit. " +
      concentrationInterpretation +
      "</p>" +

      "<h2>Operational Interpretation</h2>" +
      "<p>When profit is concentrated in a limited number of segments management attention, pricing strategy, and supplier relationships often revolve around those areas. The segments generating the majority of profit usually carry the greatest economic importance even if other segments produce larger revenue volumes. Understanding this distribution helps operators allocate management time and operational resources toward the activities that truly support the business profit engine.</p>" +

      "<h2>Structural Risk Observation</h2>" +
      "<p>If profit is concentrated the business may be structurally dependent on a narrow economic base. Pricing pressure, supplier disruption, or competitive changes affecting those segments could materially weaken overall profitability. A broader distribution of profit across segments can provide resilience by reducing reliance on a single activity or customer group.</p>" +

      "<h2>Management Questions</h2>" +
      "<p>Which revenue segments actually generate the majority of economic profit in the business? If the most profitable segment weakened how resilient would the remaining segments be? Are management resources aligned with the activities that genuinely produce profit rather than simply revenue?</p>" +

      "<p>This calculator evaluates only one narrow dimension of business structure. The broader diagnostic work performed by MJB Strategic examines the interaction between profit drivers, cost structure, capital deployment, cash flow timing, revenue concentration, supplier dynamics, and forward operating scenarios. This type of analysis requires structured financial data and careful modelling. Only a limited number of businesses are worked with at any given time because the analysis requires detailed operational understanding. If the thinking behind this diagnostic resonates with how you view your business, a deeper conversation may determine whether a more comprehensive structural review would be appropriate.</p>";

    resultContainer.innerHTML = report;

  }

  calculateButton.addEventListener("click", runDiagnostic);

  shareButton.addEventListener("click", function () {

    const url = window.location.href;

    const shareLink =
      "https://api.whatsapp.com/send?text=" +
      encodeURIComponent("Useful diagnostic tool: " + url);

    window.open(shareLink, "_blank");

  });

});