document.addEventListener("DOMContentLoaded", function () {

  const calculateButton = document.getElementById("calculateButton");
  const shareButton = document.getElementById("shareWhatsAppButton");
  const resultContainer = document.getElementById("result");

  function showError(message) {
    resultContainer.innerHTML = "<p style='color:#b91c1c;font-weight:600'>" + message + "</p>";
  }

  function runDiagnostic() {

    const segments = [];

    function readSegment(revenueId, marginId) {

      const revenue = parseFloat(document.getElementById(revenueId).value);
      const margin = parseFloat(document.getElementById(marginId).value);

      if (!isNaN(revenue) && !isNaN(margin)) {

        const profit = revenue * (margin / 100);

        segments.push({
          revenue: revenue,
          margin: margin,
          profit: profit
        });

      }

    }

    readSegment("segment1Revenue", "segment1Margin");
    readSegment("segment2Revenue", "segment2Margin");
    readSegment("segment3Revenue", "segment3Margin");
    readSegment("segment4Revenue", "segment4Margin");
    readSegment("segment5Revenue", "segment5Margin");
    readSegment("segment6Revenue", "segment6Margin");

    if (segments.length < 2) {
      showError("At least two revenue segments with revenue and margin values are required for this diagnostic.");
      return;
    }

    let totalProfit = 0;

    segments.forEach(function (segment) {
      totalProfit += segment.profit;
    });

    segments.sort(function (a, b) {
      return b.profit - a.profit;
    });

    const topSegmentProfit = segments[0].profit;

    const topTwoProfit =
      segments.length >= 2
        ? segments[0].profit + segments[1].profit
        : segments[0].profit;

    const topShare = (topSegmentProfit / totalProfit) * 100;
    const topTwoShare = (topTwoProfit / totalProfit) * 100;

    let concentrationLevel = "";

    if (topTwoShare >= 70) {
      concentrationLevel = "high";
    } else if (topTwoShare >= 50) {
      concentrationLevel = "moderate";
    } else {
      concentrationLevel = "broad";
    }

    let diagnosticSummary = "";
    let operationalInterpretation = "";
    let structuralRisk = "";

    if (concentrationLevel === "high") {

      diagnosticSummary =
        "The analysis indicates that a large proportion of total profit is generated by a small number of revenue segments. In particular, the most profitable segment contributes approximately " +
        topShare.toFixed(1) +
        "% of total estimated profit, while the two leading segments together generate roughly " +
        topTwoShare.toFixed(1) +
        "% of overall profit.";

      operationalInterpretation =
        "When profit is concentrated in a narrow set of activities, operational priorities tend to revolve around protecting those areas. Pricing discipline, supplier negotiations, and management attention often become heavily focused on the segments that generate the majority of economic return.";

      structuralRisk =
        "High profit concentration can create structural dependency. If demand weakens, margins compress, or competitive pressure emerges within the dominant segments, the broader business may not have sufficient alternative profit engines to compensate.";

    } else if (concentrationLevel === "moderate") {

      diagnosticSummary =
        "The analysis suggests that profit is moderately concentrated across the leading segments. The most profitable segment generates approximately " +
        topShare.toFixed(1) +
        "% of total profit, while the two largest segments contribute about " +
        topTwoShare.toFixed(1) +
        "% combined.";

      operationalInterpretation =
        "A moderately concentrated profit structure often indicates that the business has several meaningful profit drivers rather than relying entirely on a single activity. Operational planning still requires careful attention to the leading segments because they remain responsible for a large share of economic return.";

      structuralRisk =
        "While the business is not dependent on a single profit source, the concentration still implies that disruption in one or two segments could materially affect financial performance. Monitoring margin stability and demand patterns in these segments remains important.";

    } else {

      diagnosticSummary =
        "The analysis indicates that profit generation is distributed relatively broadly across the revenue segments. The most profitable segment contributes around " +
        topShare.toFixed(1) +
        "% of total profit, while the two largest segments together account for approximately " +
        topTwoShare.toFixed(1) +
        "%.";

      operationalInterpretation =
        "A broader profit base often reflects a business model where several activities contribute meaningfully to overall profitability. This can provide management with greater flexibility when allocating resources, adjusting pricing, or responding to changes in supplier or customer dynamics.";

      structuralRisk =
        "Although diversification reduces reliance on a single segment, management still needs to monitor margin performance across each activity. Profit distribution can change over time as cost structures, supplier terms, or customer demand evolve.";
    }

    const managementQuestions =
      "<p><strong>Management Questions</strong></p>" +
      "<p>Which segments generate the majority of economic profit rather than simply the majority of revenue?</p>" +
      "<p>If the most profitable segment experienced margin compression or demand decline, how resilient would the rest of the business be?</p>" +
      "<p>Are management time, operational resources, and capital allocation aligned with the activities that actually generate profit?</p>";

    const engagementNote =
      "<p>This calculator evaluates only one narrow dimension of business structure. In practice the broader diagnostic work performed by MJB Strategic examines how profit drivers interact with cost structure, capital deployment, cash flow timing, revenue concentration, supplier dynamics, and forward operating scenarios.</p>" +
      "<p>Understanding these relationships normally requires structured financial data, detailed management reporting, and careful modelling of operating assumptions. Because this work involves a deep operational review only a limited number of businesses are engaged with at any given time.</p>" +
      "<p>If the thinking behind this diagnostic reflects how you prefer to examine the structure of your own business you are welcome to explore whether there may be scope to work together. The website Contact page can be used to get in touch and request a quote for a deeper structural diagnostic review.</p>";

    resultContainer.innerHTML =
      "<p><strong>Diagnostic Summary</strong></p>" +
      "<p>" +
      diagnosticSummary +
      "</p>" +
      "<p><strong>Operational Interpretation</strong></p>" +
      "<p>" +
      operationalInterpretation +
      "</p>" +
      "<p><strong>Structural Risk Observation</strong></p>" +
      "<p>" +
      structuralRisk +
      "</p>" +
      managementQuestions +
      engagementNote;

  }

  calculateButton.addEventListener("click", runDiagnostic);

  shareButton.addEventListener("click", function () {

    const url = window.location.href;

    const shareLink =
      "https://api.whatsapp.com/send?text=" +
      encodeURIComponent("Useful diagnostic tool: " + url);

    window.open(shareLink, "_blank");

  });

});